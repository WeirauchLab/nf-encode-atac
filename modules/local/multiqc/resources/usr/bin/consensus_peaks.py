from multiqc.base_module import BaseMultiqcModule
from multiqc.plots import linegraph, bargraph
from multiqc import config
import logging
import glob
import json

log = logging.getLogger("multiqc")


class ConsensusPeaks(BaseMultiqcModule):
    def __init__(self):
        self.data = {}
        super(ConsensusPeaks, self).__init__(
            name="Consensus Peak Statistics",
            target="consensus_peaks",
            anchor="consensus_peaks",
            href="",
            autoformat=True,
            autoformat_type="markdown",
            info="These plots show statistics from consensus peak calling across replicates within groups.\n\n"
            ""
            "Consensus peaks are generated by comparing each replicate's peak calls within a group against their pooled peak set.\n\n"
            "The steps to generate consensus peaks are as follows:\n"
            "1. Call peaks on each replicate sample individually.\n"
            "2. Pool all replicates within a group and call peaks on the pooled data.\n"
            "3. Iterate through each peak in the pooled peak set and generate a binary matrix for each replicate indicating whether or not it was overlapping.\n"
            "4. Sum the binary matrix rows to get an overlap score for each pooled peak (i.e. how many replicates it overlaps).\n\n"
            "**NOTES**:\n\n"
            "- This is similar to, but not the same as ENCODE's Overlap reproducibility metric. All replicates within a group are tested, whereas ENCODE's metric is based on the two most similar replicates.\n"
            "- To use the consensus peak output, filter the peak file for your desired overlap score. For example, to get peaks that overlap at least 2 replicates, filter for score >= 2.\n"
            "- This consensus peak calling method is subject to change!\n"
            "- Consensus peaks are only generated for groups with 2 or more replicates.\n"
            "- Peaks found with zero overlaps are those that were found only after pooling the data.",
        )
        config_sp = getattr(config.sp, "consensus_peaks", {})
        file_pattern = config_sp.get("fn", "data/consensus_peaks/*.json")
        self.data = self.parse_files(file_pattern)

        if self.data["exact_overlaps"]:
            plot_data = self.data["exact_overlaps"]
            self.write_data_file(
                plot_data, "multiqc_consensus_peaks_exact_overlaps"
            )
            pconfig = {
                "id": "consensus_peaks_exact_overlaps",
                "title": "Exact Overlaps",
                "data_labels": [
                    {
                        "name": "Counts",
                        "xlabel": "Peaks Overlapping X samples",
                        "ylabel": "Number of Peaks",
                    }
                ],
            }
            plot_obj = bargraph.plot(
                data=plot_data,
                pconfig=pconfig,
            )
            self.add_section(
                name="Exact Overlap Distribution",
                plot=plot_obj,
                anchor="consensus_peaks_overlap_section",
                description="""
                The exact distribution shows the number (or proportion) of peaks that overlap exactly X replicates.\n
                """,
                helptext="""""",
            )

        if (
            self.data["cumulative_overlaps"]
            and self.data["cumulative_proportions"]
        ):
            plot_data = [
                self.data["cumulative_overlaps"],
                self.data["cumulative_proportions"],
            ]
            self.write_data_file(
                plot_data, "multiqc_consensus_peaks_cumulative_overlaps"
            )
            pconfig = {
                "id": "consensus_peaks_cumulative_overlaps",
                "title": "Cumulative Overlaps",
                "data_labels": [
                    {
                        "name": "Counts",
                        "xlabel": "Peaks Overlapping X OR MORE samples",
                        "ylabel": "Number of Peaks",
                    },
                    {
                        "name": "Proportions",
                        "xlabel": "Peaks Overlapping X OR MORE samples",
                        "ylabel": "Proportion of Peaks",
                    },
                ],
            }
            plot_obj = linegraph.plot(
                data=plot_data,
                pconfig=pconfig,
            )
            self.add_section(
                name="Cumulative Overlap Distribution",
                plot=plot_obj,
                anchor="consensus_peaks_cumulative_overlap_section",
                description="""
                The cumulative distribution shows the number (or proportion) of peaks that overlap X OR MORE replicates.\n
                """,
                helptext="""""",
            )

    def parse_files(self, file_pattern):
        data = {
            "exact_overlaps": {},
            "exact_proportions": {},
            "cumulative_overlaps": {},
            "cumulative_proportions": {},
        }
        found_files = [f for f in glob.iglob(file_pattern, recursive=True)]
        if not found_files:
            return None
        for f in found_files:
            with open(f) as fh:
                contents = json.load(fh)
            sample_id = contents["id"]
            data["exact_overlaps"][sample_id] = contents.get(
                "distributions", {}
            ).get("exact", {})
            data["exact_proportions"][sample_id] = contents.get(
                "proportions", {}
            ).get("exact", {})
            data["cumulative_overlaps"][sample_id] = contents.get(
                "distributions", {}
            ).get("cumulative", {})
            data["cumulative_proportions"][sample_id] = contents.get(
                "proportions", {}
            ).get("cumulative", {})

        log.info(
            "Found {} reports for {}".format(len(found_files), file_pattern)
        )

        return data
