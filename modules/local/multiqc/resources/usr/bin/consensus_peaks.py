from multiqc.base_module import BaseMultiqcModule
from multiqc.plots import linegraph
from multiqc import config
import logging
import glob
import json

log = logging.getLogger("multiqc")


class ConsensusPeaks(BaseMultiqcModule):
    def __init__(self):
        self.data = []
        super(ConsensusPeaks, self).__init__(
            name="Consensus Peak Statistics",
            target="consensus_peaks",
            anchor="consensus_peaks",
            href="",
            info="Consensus peak information and statistics.",
        )
        config_sp = getattr(config.sp, "consensus_peaks", {})
        file_pattern = config_sp.get("fn", "data/consensus_peaks/*.json")
        self.data = self.parse_files(file_pattern)
        if self.data is not None:
            self.write_data_file(self.data, "multiqc_consensus_peaks")
            plot_data = []
            pconfig = {
                "id": "consensus_peaks_overlap_distribution",
                "title": "Consensus Peaks Overlap Distribution",
                "data_labels": [],
            }
            if self.data["exact_distribution"]:
                pconfig["data_labels"].append(
                    {
                        "name": "Exact Distribution",
                        "xlabel": "Peaks Overlapping X samples (exactly)",
                        "ylabel": "Number of Peaks",
                    }
                )
                plot_data.append(self.data["exact_distribution"])
            if self.data["cumulative_distribution"]:
                pconfig["data_labels"].append(
                    {
                        "name": "Cumulative Distribution",
                        "xlabel": "Peaks Overlapping X OR MORE samples",
                        "ylabel": "Number of Peaks",
                    }
                )
                plot_data.append(self.data["cumulative_distribution"])
            if self.data["exact_proportion"]:
                pconfig["data_labels"].append(
                    {
                        "name": "Exact Proportion",
                        "xlabel": "Peaks Overlapping X samples (exactly)",
                        "ylabel": "Proportion of Peaks",
                    }
                )
                plot_data.append(self.data["exact_proportion"])
            if self.data["cumulative_proportion"]:
                pconfig["data_labels"].append(
                    {
                        "name": "Cumulative Proportion",
                        "xlabel": "Peaks Overlapping X OR MORE samples",
                        "ylabel": "Proportion of Peaks",
                    }
                )
                plot_data.append(self.data["cumulative_proportion"])
            plot_obj = linegraph.plot(
                data=plot_data,
                pconfig=pconfig,
            )
            self.add_section(
                name="Consensus Peaks Overlap Distribution",
                plot=plot_obj,
                anchor="consensus_peaks_overlap_section",
                description="""
                This plot shows the distribution of consensus peaks based on how many samples they overlap.\n
                Consensus peaks are generated by comparing each replicate's peak calls within a group against their pooled peak set.\n
                \n
                **Exact distribution**\n
                    The number (or proportion) of peaks that overlap exactly X replicates.\n
                
                **Cumulative distribution**\n
                    The number (or proportion) of peaks that overlap X OR MORE replicates.\n
                \n
                **NOTES**:
                - This is similar to, but not the same as ENCODE's Overlap reproducibility metric. All replicates within a group are tested, whereas ENCODE's metric is based on the two most similar replicates.\n
                - To use the consensus peak output, filter the peak file for your desired overlap score. For example, to get peaks that overlap at least 2 replicates, filter for score >= 2.
                - This consensus peak calling method is subject to change!
                - Consensus peaks are only generated for groups with 2 or more replicates.
                """,
                helptext="""""",
            )

    def parse_files(self, file_pattern):
        data = {
            "exact_distribution": {},
            "cumulative_distribution": {},
            "exact_proportion": {},
            "cumulative_proportion": {},
        }
        found_files = [f for f in glob.iglob(file_pattern, recursive=True)]
        if not found_files:
            return None
        for f in found_files:
            with open(f) as fh:
                contents = json.load(fh)
            sample_id = contents["id"]
            data["exact_distribution"][sample_id] = contents.get(
                "distributions", {}
            ).get("exact", {})
            data["cumulative_distribution"][sample_id] = contents.get(
                "distributions", {}
            ).get("cumulative", {})
            data["exact_proportion"][sample_id] = contents.get(
                "proportions", {}
            ).get("exact", {})
            data["cumulative_proportion"][sample_id] = contents.get(
                "proportions", {}
            ).get("cumulative", {})

        log.info(
            "Found {} reports for {}".format(len(found_files), file_pattern)
        )

        return data
